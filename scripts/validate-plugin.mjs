#!/usr/bin/env node
// Plugin validation — simulates what /materia-init materializes from the
// `materia` plugin's bundled scaffold, then verifies it. No network, no AI.
// The plugin lives at plugins/materia/: skills/ (all pipeline skills, at the
// plugin root per the Claude Code plugin spec) and scaffold/ (the MATERIA.md +
// CLAUDE.md + docs/ + check-docs.mjs bundle that /materia-init writes into a
// user repo). Three layers:
//  1. Materialization sim: copy plugins/materia/scaffold/ into the post-init
//     layout, stub the five standards /materia-init always generates, run
//     check-docs.mjs (links + anchors + style) over the result.
//  2. § audit: every segment of every `MATERIA.md § A § B` reference chain
//     across plugins/materia/ names a real heading in MATERIA.md, plus mirror
//     pins (§ Skill routing rows that must carry an equal tier).
//  3. Slot hygiene: {{slot}} markers may exist only in the slotted templates
//     (MATERIA.md, CLAUDE.md, docs skeleton) — never in skills.
// Exits non-zero with the failures listed. Run: node scripts/validate-plugin.mjs
import { cpSync, mkdirSync, mkdtempSync, rmSync, writeFileSync, readFileSync, readdirSync, statSync } from 'node:fs'
import { join } from 'node:path'
import { tmpdir } from 'node:os'
import { spawnSync } from 'node:child_process'

process.chdir(join(import.meta.dirname, '..'))
let failures = 0
const fail = (msg) => { console.error(`  ✗ ${msg}`); failures++ }

// ---- 1. materialization sims + check-docs ----------------------------------
// Two profiles: a full UI repo, and a pruned non-UI repo (materia-init's
// § Phase 4 prune set removed, no UI standards generated). Both must pass.
const UI_PRUNE = ['materia-design', 'materia-ui-test-plan', 'materia-ui-review', 'materia-ui-inspection']
const runSim = (label, { prune = [], standards }) => {
  const sim = mkdtempSync(join(tmpdir(), 'materia-sim-'))
  try {
    mkdirSync(join(sim, '.claude'), { recursive: true })
    mkdirSync(join(sim, 'scripts'), { recursive: true })
    cpSync('plugins/materia/scaffold/docs', join(sim, 'docs'), { recursive: true })
    cpSync('plugins/materia/skills', join(sim, '.claude/skills'), { recursive: true })
    for (const p of prune) {
      rmSync(join(sim, '.claude/skills', p), { recursive: true, force: true })
      // mimic init: a pruned producer is deregistered from the queue tables
      for (const q of ['docs/specs/_proposed/README.md', 'docs/bugs/_reports/README.md']) {
        const qp = join(sim, q)
        writeFileSync(qp, readFileSync(qp, 'utf8').split('\n').filter((l) => !l.includes(p)).join('\n'))
      }
    }
    cpSync('plugins/materia/scaffold/scripts/check-docs.mjs', join(sim, 'scripts/check-docs.mjs'))
    cpSync('plugins/materia/scaffold/CLAUDE.md', join(sim, 'CLAUDE.md'))
    cpSync('plugins/materia/scaffold/MATERIA.md', join(sim, 'MATERIA.md'))
    for (const f of standards)
      writeFileSync(join(sim, `docs/standards/${f}.md`), '# stub — generated by /materia-init\n')
    const r = spawnSync('node', ['scripts/check-docs.mjs'], { cwd: sim, encoding: 'utf8' })
    if (r.status !== 0) {
      fail(`materialization sim (${label}) failed check-docs:`)
      console.error(r.stdout + r.stderr)
    } else {
      console.log(`  ✓ materialization sim (${label}): ` + (r.stdout.trim().split('\n').pop() ?? 'clean'))
    }
  } finally {
    rmSync(sim, { recursive: true, force: true })
  }
}
runSim('UI repo', { standards: ['architecture', 'testing', 'workflow', 'visual-language', 'ui-components'] })
runSim('non-UI repo, UI skills pruned', { prune: UI_PRUNE, standards: ['architecture', 'testing', 'workflow'] })

// ---- 1b. stage-numbering canon ----------------------------------------------
// The status templates are the source of truth; skills that hard-code a row
// number must agree. Both sides are pinned here so drift fails CI.
const canon = [
  ['plugins/materia/scaffold/docs/specs/_templates/status.md', '- [ ] 4. architecture'],
  ['plugins/materia/scaffold/docs/specs/_templates/status.md', '- [ ] 5. plan-tasks'],
  ['plugins/materia/scaffold/docs/specs/_templates/status.md', '- [ ] 9. finalize'],
  ['plugins/materia/scaffold/docs/bugs/_templates/status.md', '- [ ] 3. plan-tasks'],
  ['plugins/materia/scaffold/docs/bugs/_templates/status.md', '- [ ] 6. docs-sync'],
  ['plugins/materia/scaffold/docs/bugs/_templates/status.md', '- [ ] 7. docs-audit'],
  ['plugins/materia/scaffold/docs/bugs/_templates/status.md', '- [ ] 8. finalize'],
  ['plugins/materia/skills/materia-architecture/SKILL.md', 'tick stage 4 in `STATUS.md`'],
  ['plugins/materia/skills/materia-plan-tasks/SKILL.md', 'tick stage 5 in `STATUS.md`'],
  ['plugins/materia/skills/materia-finalize/SKILL.md', 'row 9 in the spec template, row 8 in the bug template'],
  ['plugins/materia/skills/materia-fix-bug/SKILL.md', 'Tick **stage 3**, not stage 5'],
  ['plugins/materia/skills/materia-fix-bug/SKILL.md', '7. **docs-audit**'],
]
for (const [f, needle] of canon)
  if (!readFileSync(f, 'utf8').includes(needle)) fail(`stage canon: ${f} missing expected text: "${needle}"`)
console.log(`  ✓ stage-numbering canon: ${canon.length} pins hold`)

// ---- helpers ---------------------------------------------------------------
const mdFiles = []
const walk = (p) => {
  for (const e of readdirSync(p)) {
    const fp = join(p, e)
    if (statSync(fp).isDirectory()) walk(fp)
    else if (fp.endsWith('.md')) mdFiles.push(fp)
  }
}
walk('plugins/materia')

// ---- 2. § audit -------------------------------------------------------------
// Every segment of a `MATERIA.md § A § B` chain must name a real heading AND,
// past the first, be a subsection of its predecessor — so a mis-scoped pointer
// like `§ Gate § Model set` (Model set is a child of Tiers, not Gate) fails,
// not just an unknown segment. References wrap across lines, so audit over
// whitespace-normalized text and match each segment against the known heading
// set (longest first, at a word boundary) rather than guessing terminators —
// which also stops the capture from swallowing trailing prose.
const heads = []
const parentOf = new Map() // h3 heading -> its parent h2, for the nesting check
let curH2 = null
for (const ln of readFileSync('plugins/materia/scaffold/MATERIA.md', 'utf8').split('\n')) {
  const m2 = /^## (.+)$/.exec(ln), m3 = /^### (.+)$/.exec(ln)
  if (m2) { curH2 = m2[1].trim(); heads.push(curH2) }
  else if (m3) { const h = m3[1].trim(); heads.push(h); if (curH2) parentOf.set(h, curH2) }
}
const headsByLen = [...heads].sort((a, b) => b.length - a.length)
// the known heading that `text` begins with (word-boundary terminated), or null
const leadingHead = (text) =>
  headsByLen.find((h) => text === h || (text.startsWith(h) && /[^A-Za-z0-9]/.test(text[h.length]))) ?? null
let refs = 0
for (const f of mdFiles) {
  const norm = readFileSync(f, 'utf8').replace(/\s+/g, ' ')
  for (const start of norm.matchAll(/MATERIA\.md[`"']?/g)) {
    let pos = start.index + start[0].length
    let prev = null
    let sm
    while ((sm = /^\s*§\s*/.exec(norm.slice(pos)))) {
      const after = pos + sm[0].length
      if (!/^[A-Za-z]/.test(norm.slice(after))) break // `§` not followed by a name → not a section ref
      pos = after
      refs++
      const h = leadingHead(norm.slice(pos))
      if (!h) {
        const bad = norm.slice(pos, pos + 30).replace(/[.,;()`|<>—'"].*$/, '').trim()
        fail(`${f}: MATERIA.md § "${bad}…" has no matching heading in plugins/materia/scaffold/MATERIA.md`)
        break // chain integrity is lost past an unknown segment
      }
      if (prev && parentOf.get(h) !== prev) {
        fail(`${f}: MATERIA.md § "${prev} § ${h}" — "${h}" is not a subsection of "${prev}"`)
        break
      }
      prev = h
      pos += h.length
    }
  }
}
console.log(`  ✓ § audit: ${refs} MATERIA.md § references (nested, all chain segments) checked against ${heads.length} headings`)

// ---- 2b. mirror pins --------------------------------------------------------
// Rows in § Skill routing that intentionally duplicate another row's tier are
// pinned equal here so an edit to one that misses the other fails CI.
const routingRow = (label) => {
  const src = readFileSync('plugins/materia/scaffold/MATERIA.md', 'utf8')
  const re = new RegExp(`^\\|\\s*\`${label.replace(/[.*+?^${}()|[\]\\/]/g, '\\$&')}\`\\s*\\|(.+)$`, 'm')
  const m = src.match(re)
  if (!m) return null
  return m[1].split('|').slice(0, 3).map((c) => c.trim()) // [Model, Effort, Fallback Model]
}
const MIRRORS = [['materia-ui-review', 'ship-spec: review/ui']]
for (const [a, b] of MIRRORS) {
  const ra = routingRow(a), rb = routingRow(b)
  if (!ra) fail(`mirror pin: § Skill routing row \`${a}\` not found`)
  else if (!rb) fail(`mirror pin: § Skill routing row \`${b}\` not found`)
  else if (ra.join('/') !== rb.join('/'))
    fail(`mirror pin: \`${a}\` (${ra.join('/')}) and \`${b}\` (${rb.join('/')}) must carry the same tier`)
}
console.log(`  ✓ mirror pins: ${MIRRORS.length} routing-row mirror(s) hold`)

// ---- 3. slot hygiene ---------------------------------------------------------
// materia-init is exempt: it's the skill that documents the slot-filling
// mechanism itself, so its prose legitimately backtick-quotes `{{slots}}` when
// describing the process (never as a literal unfilled marker it ships). This
// mirrors its pre-migration exemption, when it lived outside templates/skills/.
for (const f of mdFiles.filter((f) => f.includes('plugins/materia/skills/') && !f.includes('plugins/materia/skills/materia-init/'))) {
  const s = readFileSync(f, 'utf8')
  const idx = s.indexOf('{{')
  if (idx !== -1) fail(`${f} carries a {{slot}} marker — skills ship slot-free (slots live in MATERIA.md/CLAUDE.md/docs)`)
}
console.log('  ✓ slot hygiene: no {{slot}} markers in skills')

if (failures) {
  console.error(`\n${failures} validation failure(s).`)
  process.exit(1)
}
console.log('\nplugin validation: all clean.')
