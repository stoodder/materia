#!/usr/bin/env node
// Template validation — simulates what /materia-init materializes, then
// verifies it. No network, no AI. Three layers:
//  1. Materialization sim: copy templates/ into the post-init layout, stub
//     the five standards /materia-init always generates, run check-docs.mjs
//     (links + anchors + style) over the result.
//  2. § audit: every `MATERIA.md § X` reference across templates/ and
//     .claude/ names a real heading in templates/MATERIA.md.
//  3. Slot hygiene: {{slot}} markers may exist only in the slotted templates
//     (MATERIA.md, CLAUDE.md, docs skeleton) — never in skills.
// Exits non-zero with the failures listed. Run: node scripts/validate-template.mjs
import { cpSync, mkdirSync, mkdtempSync, rmSync, writeFileSync, readFileSync, readdirSync, statSync } from 'node:fs'
import { join } from 'node:path'
import { tmpdir } from 'node:os'
import { spawnSync } from 'node:child_process'

process.chdir(join(import.meta.dirname, '..'))
let failures = 0
const fail = (msg) => { console.error(`  ✗ ${msg}`); failures++ }

// ---- 1. materialization sims + check-docs ----------------------------------
// Two profiles: a full UI repo, and a pruned non-UI repo (materia-init's
// § Phase 4 prune set removed, no UI standards generated). Both must pass.
const UI_PRUNE = ['materia-design', 'materia-ui-test-plan', 'materia-ui-review', 'materia-ui-inspection']
const runSim = (label, { prune = [], standards }) => {
  const sim = mkdtempSync(join(tmpdir(), 'materia-sim-'))
  try {
    mkdirSync(join(sim, '.claude'), { recursive: true })
    mkdirSync(join(sim, 'scripts'), { recursive: true })
    cpSync('templates/docs', join(sim, 'docs'), { recursive: true })
    cpSync('templates/skills', join(sim, '.claude/skills'), { recursive: true })
    for (const p of prune) {
      rmSync(join(sim, '.claude/skills', p), { recursive: true, force: true })
      // mimic init: a pruned producer is deregistered from the queue tables
      for (const q of ['docs/specs/_proposed/README.md', 'docs/bugs/_reports/README.md']) {
        const qp = join(sim, q)
        writeFileSync(qp, readFileSync(qp, 'utf8').split('\n').filter((l) => !l.includes(p)).join('\n'))
      }
    }
    cpSync('templates/scripts/check-docs.mjs', join(sim, 'scripts/check-docs.mjs'))
    cpSync('templates/CLAUDE.md', join(sim, 'CLAUDE.md'))
    cpSync('templates/MATERIA.md', join(sim, 'MATERIA.md'))
    for (const f of standards)
      writeFileSync(join(sim, `docs/standards/${f}.md`), '# stub — generated by /materia-init\n')
    const r = spawnSync('node', ['scripts/check-docs.mjs'], { cwd: sim, encoding: 'utf8' })
    if (r.status !== 0) {
      fail(`materialization sim (${label}) failed check-docs:`)
      console.error(r.stdout + r.stderr)
    } else {
      console.log(`  ✓ materialization sim (${label}): ` + (r.stdout.trim().split('\n').pop() ?? 'clean'))
    }
  } finally {
    rmSync(sim, { recursive: true, force: true })
  }
}
runSim('UI repo', { standards: ['architecture', 'testing', 'workflow', 'visual-language', 'ui-components'] })
runSim('non-UI repo, UI skills pruned', { prune: UI_PRUNE, standards: ['architecture', 'testing', 'workflow'] })

// ---- 1b. stage-numbering canon ----------------------------------------------
// The status templates are the source of truth; skills that hard-code a row
// number must agree. Both sides are pinned here so drift fails CI.
const canon = [
  ['templates/docs/specs/_templates/status.md', '- [ ] 4. architecture'],
  ['templates/docs/specs/_templates/status.md', '- [ ] 5. plan-tasks'],
  ['templates/docs/specs/_templates/status.md', '- [ ] 9. finalize'],
  ['templates/docs/bugs/_templates/status.md', '- [ ] 3. plan-tasks'],
  ['templates/docs/bugs/_templates/status.md', '- [ ] 6. docs-sync'],
  ['templates/docs/bugs/_templates/status.md', '- [ ] 7. docs-audit'],
  ['templates/docs/bugs/_templates/status.md', '- [ ] 8. finalize'],
  ['templates/skills/materia-architecture/SKILL.md', 'tick stage 4 in `STATUS.md`'],
  ['templates/skills/materia-plan-tasks/SKILL.md', 'tick stage 5 in `STATUS.md`'],
  ['templates/skills/materia-finalize/SKILL.md', 'row 9 in the spec template, row 8 in the bug template'],
  ['templates/skills/materia-fix-bug/SKILL.md', 'Tick **stage 3**, not stage 5'],
  ['templates/skills/materia-fix-bug/SKILL.md', '7. **docs-audit**'],
]
for (const [f, needle] of canon)
  if (!readFileSync(f, 'utf8').includes(needle)) fail(`stage canon: ${f} missing expected text: "${needle}"`)
console.log(`  ✓ stage-numbering canon: ${canon.length} pins hold`)

// ---- helpers ---------------------------------------------------------------
const mdFiles = []
const walk = (p) => {
  for (const e of readdirSync(p)) {
    const fp = join(p, e)
    if (statSync(fp).isDirectory()) walk(fp)
    else if (fp.endsWith('.md')) mdFiles.push(fp)
  }
}
walk('templates'); walk('.claude')

// ---- 2. § audit -------------------------------------------------------------
const heads = new Set(
  [...readFileSync('templates/MATERIA.md', 'utf8').matchAll(/^#{2,3} (.+)$/gm)].map((m) => m[1].trim()),
)
const SECREF = /MATERIA\.md[`"']?\s*§\s*([A-Za-z][A-Za-z :&-]*?)(?=\s*[§.,;()`|<>\n—'"-]|'s)/g
let refs = 0
for (const f of mdFiles) {
  const lines = readFileSync(f, 'utf8').split('\n')
  lines.forEach((line, i) => {
    for (const m of line.matchAll(SECREF)) {
      refs++
      const sec = m[1].trim()
      if ([...heads].some((h) => h === sec || sec.startsWith(h) || h.startsWith(sec))) continue
      fail(`${f}:${i + 1} MATERIA.md § "${sec}" has no matching heading in templates/MATERIA.md`)
    }
  })
}
console.log(`  ✓ § audit: ${refs} MATERIA.md § references checked against ${heads.size} headings`)

// ---- 3. slot hygiene ---------------------------------------------------------
for (const f of mdFiles.filter((f) => f.includes('templates/skills/'))) {
  const s = readFileSync(f, 'utf8')
  const idx = s.indexOf('{{')
  if (idx !== -1) fail(`${f} carries a {{slot}} marker — skills ship slot-free (slots live in MATERIA.md/CLAUDE.md/docs)`)
}
console.log('  ✓ slot hygiene: no {{slot}} markers in skills')

if (failures) {
  console.error(`\n${failures} validation failure(s).`)
  process.exit(1)
}
console.log('\ntemplate validation: all clean.')
